/**
 * Резолвит good_id товара-сертификата для филиала. Сначала — программа по
 * названию, иначе — номинал по сумме. null, если соответствия нет.
 */
export function resolveGoodId(
  companyId: number,
  input: { nominalKzt: number; programTitle?: string | null },
): number | null {
  const c = String(companyId);
  if (input.programTitle) {
    const byTitle = PROGRAM_GOODS[c]?.[input.programTitle];
    if (byTitle) return byTitle;
  }
  return NOMINAL_GOODS[c]?.[String(input.nominalKzt)] ?? null;
}

/** Складские параметры филиала или null. */
export function branchParams(companyId: number): BranchParams | null {
  return BRANCH_PARAMS[String(companyId)] ?? null;
}
